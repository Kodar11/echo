export function isStopWord(term: string, language: string | null): boolean {
  if (!language) return false;
  const list = STOP_WORDS[language.toLowerCase() as SupportedLanguage];
  if (!list) return false;
  return list.has(term);
}

export function getStopWords(language: string): string[] {
  const list = STOP_WORDS[language.toLowerCase() as SupportedLanguage];
  return list ? Array.from(list) : [];
}
